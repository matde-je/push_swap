#include "push_swap.h"

int get_max(int *arr, int n)
{
	int	i;
	int	max;

	max = arr[0];
	i = 1;
	while (i++ < n) 
	{
		if (arr[i] > max)
            max = arr[i];
	}
	return (max);
}
